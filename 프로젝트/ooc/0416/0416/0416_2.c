#include <stdio.h>

int sum() {
	int result = 0;         //1부터 100까지 더한 값을 저장할 변수
	for (int i = 1; i <= 100; i++) {
		result += i;
		printf("%d", i);
		if (i != 100) {
			printf("+");
		}
	}
		printf("\n");
		return result;

}




